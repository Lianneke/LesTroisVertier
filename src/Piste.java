public class Piste {

    private String name;
    private String color;
    private int hight;

    public Piste(String name, String color, int hight) {
        this.name = name;
        this.color = color;
        this.hight = hight;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }

    public int getHight() {
        return hight;
    }
}
